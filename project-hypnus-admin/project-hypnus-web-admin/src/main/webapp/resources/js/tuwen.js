// JavaScript Document

$(document).ready(function(e) {
	
    //新增多图文
	$(".xw_tuwenselect").click(function(){
		$(".xw_tuwenselect").removeClass("tuwenselected");
		$(this).addClass("tuwenselected");
		});
		
	$(".xw_tuwenselect").mouseenter(function(){
		$(".tuwenzhezhao").slideUp();
		$(this).children(".tuwenzhezhao").slideDown();
		});
	$(".xw_tuwenselect").mouseleave(function(){
		$(".tuwenzhezhao").slideUp();
		});
});